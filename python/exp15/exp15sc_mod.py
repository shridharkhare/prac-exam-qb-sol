# package for scientific calculator

import math


def add(a, b):
    return a + b


def sub(a, b):
    return a - b


def mul(a, b):
    return a * b


def div(a, b):
    return a / b


def sqrt(a):
    return math.sqrt(a)


def power(a, b):
    return math.pow(a, b)


def sin(a):
    return math.sin(a)


def cos(a):
    return math.cos(a)


def tan(a):
    return math.tan(a)


def log(a):
    return math.log(a)


def log10(a):
    return math.log10(a)


def exp(a):
    return math.exp(a)


def factorial(a):
    return math.factorial(a)
